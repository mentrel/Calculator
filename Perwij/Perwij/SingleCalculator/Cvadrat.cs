namespace Perwij.SingleCalculator
{
    public class Cvadrat : ISingle
    {
        /// <summary>
        /// Функция возведения введённого числа в квадрат
        /// </summary>
        /// <param name="one"></param>
        /// <returns></returns>
        public double CreateCalculate(double one)
        {
            return one * one;
        }

    }
}
